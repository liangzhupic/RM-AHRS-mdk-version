void Delayus(uint16_t t)
{
    #define timer_max 0xffff
    uint64_t t_t;
    uint64_t T= LL_TIM_GetCounter(htim2.Instance)+t;
    if(T>timer_max)
    {
        t_t=T-timer_max;
    }
    else {
        t_t=T;
    }
    while (LL_TIM_GetCounter(htim2.Instance)<t_t) {
        //wait ...
    }
}

void TimeCounter(TimeCounter_t *t,int ctrl)
{
    if(ctrl==1)
    {
        t->start=LL_TIM_GetCounter(htim2.Instance);
    }
    else
    {
        t->stop=LL_TIM_GetCounter(htim2.Instance);
        t->interval=t->stop-t->start;
    }
}

